public class Playoffs {
    int hostImg;
    String hostName;
    String hostScore;

    int guestImg;
    String guestName;
    String guestScore;

    Playoffs(int hostImg, String hostName, String hostScore, int guestImg, String guestName, String guestScore){
        hostImg = this.hostImg;
        hostName = this.hostName;
        hostScore = this.hostScore;

        guestImg = this.guestImg;
        guestName = this.guestName;
        guestScore = this.guestScore;
    }


}
